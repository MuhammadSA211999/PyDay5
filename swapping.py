# Swapping: swapping hocce ekadik variable er value nijeder modhye exchange kora.
a=10
b=20
# manually value exchangeing:
temp=a # temp=10 ,temp er moddhye a er value assign korar sath sathe a khali hoye gelo.
a=b # a=20, ekhon b er value a er moddhye assign hoye gelo.
b=temp # b=10, ekhane temp er value ortath a er vale 10 b te assign hoye gelo.
print(a,b) # a=20, b=10 [value exchange hoye gelo]

